public class MathUtils {
    public int add(int a, int b) {
        return a + b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double div(int a, int b) {
        return a / b;
    }

    public double computeCircleRadius(double radius) {
        return Math.PI * Math.pow(radius, 2);
    }
}
